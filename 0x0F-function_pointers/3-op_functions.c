#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a:the first number
 * @b:the second number
 * Return:the addition
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a:the first number
 * @b:the second number
 * Return:the difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a:the first number
 * @b:the second number
 * Return:the multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a:the first number
 * @b:the second number
 * Return:the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divide two numbers
 * @a:the first number
 * @b:the second number
 * Return:the reminder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
